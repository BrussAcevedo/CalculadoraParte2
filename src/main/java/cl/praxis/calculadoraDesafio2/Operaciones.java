package cl.praxis.calculadoraDesafio2;

public class Operaciones {

	
	public int sumar(int a, int b) {
		int result = a+b;
		return result;
		
		
	}
	public int restar(int a, int b) {
		int result = a-b;
		return result;
	}
	public int multiplicar(int a, int b) {
		int result = a*b;
		return result;
	}
	public double  dividir(double a, double b) {
		double result = a/b;
		return result;
	}

	
}
